/**
 * op_add - function that adds two int numbers
 * @a: the first int num
 * @b: the second int num
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - gets the difference of a and b
 * @a: the first number
 * @b: the second number
 *
 * Return: a int number
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - get the product of a and b
 * @a: int num
 * @b: int num
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - gets the division of a by b
 * @a: int num
 * @b: int num
 *
 * Return: int num
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets the reminder of the division of a by b
 * @a: int num
 * @b: int num
 *
 * Return: int num
 */
int op_mod(int a, int b)
{
	return (a % b);
}
